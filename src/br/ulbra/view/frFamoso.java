    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

            String i = txtPesquisa.getText();
            String nomeEditado = JOptionPane.showInputDialog("Qualo o nome do famoso?");
            if (!i.isEmpty()) {
                if (!txtPesquisa.equals("") && p.temEspaco(nomeEditado)) {
                    p.update(p.search(txtPesquisa.getText()), p.searchAlfabetico(txtPesquisa.getText()),
                           nomeEditado);
                    if (radioAlfabetico.isSelected()) {
                        txtResposta.setText(p.toStringAlfabetico());
                    } else {
                        txtResposta.setText(p.toString());
                    }
                    txtPesquisa.setText(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Insira nome e sobrenome!");
                }
            }else{
                JOptionPane.showMessageDialog(null, "A caixa de texto está vazia");
            }
    }//GEN-LAST:event_btnEditarActionPerformed

