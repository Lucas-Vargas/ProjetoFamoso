    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        String famoso = JOptionPane.showInputDialog(null, "Informe o nome do famoso!").toUpperCase();
        txtPesquisa.setText(p.getFamoso(p.search(famoso.toUpperCase())));


    }//GEN-LAST:event_btnPesquisaActionPerformed
    BdFamoso p = new BdFamoso();

    public String getList() {
        if (radioInsercao.isSelected()) {
            return p.toString();
        } else {
            return p.toStringAlfabetico();
        }
    }


    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txtFamoso.getText().equals("")) {
            if (p.temEspaco(txtFamoso.getText())) {
                p.save(txtFamoso.getText());
                txtResposta.setText(getList());
                txtFamoso.setText(null);
                txtFamoso.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Insira nome e sobrenome");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite o nome do famoso");
            txtFamoso.requestFocus();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Tem certeza ?", "excluir",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza MESMO?", "excluir",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                txtFamoso.setText(null);
                txtFamoso.setText(null);
                txtResposta.setText(null);
                txtPesquisa.setText(null);
                p.reset();
                JOptionPane.showMessageDialog(null, "O programa foi limpo!");
            } else {
                JOptionPane.showMessageDialog(null, "Exclusao cancelada!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Exclusao cancelada!");
        }


    }//GEN-LAST:event_btnLimparActionPerformed

